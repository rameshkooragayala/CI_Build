using System;

public class MediaContent
{
    private String name;

    public String Name
    {
        get { return name; }
        set { name = value; }
    }
    private String type;

    public String Type
    {
        get { return type; }
        set { type = value; }
    }
    private String encoding;

    public String Encoding
    {
        get { return encoding; }
        set { encoding = value; }
    }

    private String device;

    public String Device
    {
        get { return device; }
        set { device = value; }
    }

    private String folder;
    public String Folder
    {
        get { return folder; }
        set { folder = value; }
    }
}
